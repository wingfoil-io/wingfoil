//! Ahead-of-time code generation: compile a wired graph's schedule to Rust.
//!
//! [`generate`] walks a wired graph and emits a standalone Rust source file
//! containing a *static schedule*: the dispatch order, tick propagation and
//! active edges are compiled into straight-line code instead of being
//! interpreted by the engine's dirty-list/layer machinery at runtime. Node
//! behaviour (including closures captured by `map`, `filter`, etc.) stays in
//! your wiring code — the generated `run` function takes the same root nodes
//! that [`Graph`](crate::Graph) would and re-wires them deterministically.
//!
//! What is compiled away: per-cycle layer vectors, dirty-node lists,
//! downstream edge iteration and node-index hashing. What remains at runtime
//! is [`StaticRuntime`], a thin shim over the engine's clock/callback
//! management (scheduled callbacks, ready callbacks from threaded sources,
//! lifecycle and error contexts) which shares its implementation with the
//! interpreted engine so the two cannot drift semantically.
//!
//! # Workflow
//!
//! ```
//! use std::time::Duration;
//! use wingfoil::*;
//!
//! // Your wiring function, unchanged.
//! fn wire() -> std::rc::Rc<dyn Node> {
//!     ticker(Duration::from_millis(10)).count().print().as_node()
//! }
//!
//! // One-off (a small bin, example, or build step): emit the static runner.
//! let source = wingfoil::codegen::generate(vec![wire()], &Default::default()).unwrap();
//! assert!(source.starts_with("// @generated by wingfoil::codegen"));
//! // std::fs::write("src/generated.rs", source)?;
//!
//! // In the application, the generated file replaces Graph::run:
//! //     mod generated;
//! //     generated::run(vec![wire()], RunMode::RealTime, RunFor::Forever)?;
//! ```
//!
//! The generated `run` guards against wiring drift: it re-derives a
//! fingerprint of the topology (node types, edges, active flags) and fails
//! with a "regenerate" error if it no longer matches the compiled schedule.
//!
//! # Two generators
//!
//! - [`generate`] (this page) — compiles the *schedule*. Core node kinds
//!   (`map`, `filter`, `fold`, `constant`, `sample`, `merge`) are executed
//!   through typed handles with static dispatch (`inline: true`, the
//!   default); every other node falls back to dynamic dispatch. Works with
//!   **any** graph, and node state stays in the wired nodes (so `peek_value`
//!   handles keep working after the run).
//! - [`generate_standalone`] — compiles the *whole graph*: no executor, no
//!   nodes, state in monomorphized locals, and the user's closures supplied
//!   via a generated `Inputs` struct so the compiler inlines their bodies
//!   into the schedule. Restricted to the core node kinds above plus
//!   `ticker`; see [`standalone`](self) module docs for details.
//!
//! # Limitations
//!
//! - Dynamic graph changes (`add_upstream` / `remove_node`, behind the
//!   `dynamic-graph` feature) are rejected at runtime — a compiled schedule
//!   cannot change shape. Use the interpreted engine for those graphs.
//! - The wiring function must produce the same topology as at generation
//!   time; the fingerprint guard turns a mismatch into an error at startup.
//! - Each cycle evaluates one dispatch line per node. Quiet nodes cost only
//!   a predicted register test (`dirty` checks are emitted solely for node
//!   kinds that can actually receive callbacks), but the floor is still
//!   O(nodes) per cycle — whereas the interpreted engine's dirty-lists do
//!   work proportional to *ticking* nodes only. Static runners target
//!   small-to-medium hot graphs; for very large, sparsely-ticking graphs
//!   measure both engines.

